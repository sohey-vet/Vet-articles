def parse_lists(text):
    lists = {"A": {}, "B": {}, "C": {}}
    current_list = None
    current_race = None
    
    for line in text.splitlines():
        line = line.strip()
        if not line:
            continue
            
        if line.startswith("リスト"):
            current_list = line.replace("リスト", "").strip()
            current_race = None
            continue
            
        if line.startswith("的中率順位"):
            continue
        if re.match(r'^[0-9,\s]+$', line): # 的中率順位の数値行
            continue
            
        # レース名か馬名の判断
        # レース名: 東京1R, 小倉2 など (数字+R または 数字)
        m_race = re.match(r'^([小東阪京中札函福新][倉京神都山京幌館島潟]\d+R?)$', line)
        if m_race:
            current_race = m_race.group(1)
            if not current_race.endswith("R"):
                current_race += "R"
            if current_race not in lists[current_list]:
                lists[current_list][current_race] = []
            continue
            
        # 馬名: 数字+文字
        m_horse = re.match(r'^(\d+)(.+)$', line)
        if m_horse and current_race:
            uno = int(m_horse.group(1))
            hname = m_horse.group(2).strip()
            lists[current_list][current_race].append((uno, hname))

    return lists

