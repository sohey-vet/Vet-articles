CIRCLE_NUMBERS = "①②③④⑤⑥⑦⑧⑨⑩⑪⑫⑬⑭⑮⑯⑰⑱⑲⑳㉑㉒㉓㉔㉕㉖㉗㉘㉙㉚㉛㉜㉝㉞㉟㊱"
week_map = {char: int(i+1) for i, char in enumerate(CIRCLE_NUMBERS)}

def get_mapping(category, mapping_dict, default_msg):
    for key, val in mapping_dict.items():
        if key in category:
            return val
    return default_msg

base_dir = r"c:\Users\souhe\Desktop\VetEvidence_SNS_Drafts"
folders = [f for f in os.listdir(base_dir) if os.path.isdir(os.path.join(base_dir, f))]

weeks_data = {i: {} for i in range(1, 37)}

for f in folders:
    match = re.match(r"^([①-㊱])([月水金])_([^_]+)_(.+)$", f)
    if match:
        week_char, day, cat, title = match.groups()
        week_num = week_map[week_char]
        title_clean = title.replace(".md", "")
        
        weeks_data[week_num][day] = {
            "title": title_clean,
            "summary_5A": get_mapping(cat, CATEGORY_SUMMARIES, "対象疾患の最新ドキュメントとご家庭でのケアのポイント。"),
            "summary_2": get_mapping(cat, CATEGORY_CONCLUSIONS, "対象疾患に対し、根拠に基づいた最適解を選択することが不可欠です。")
        }

