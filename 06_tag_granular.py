def make_tag_span(text: str) -> Tag:
    span = Tag(name='span')
    span['class'] = ['tag', 'tag--primary']
    span.string = text
    return span

def add_tags_if_missing(tags_div, new_tags: list[str]):
    existing = {s.text.strip() for s in tags_div.find_all('span', class_='tag')}
    changed = False
    for t in new_tags:
        if t not in existing:
            tags_div.append(make_tag_span(t))
            existing.add(t)
            changed = True
    return changed

